express内置的中间件
basicAuth使用一个具有用户名参数与密码参数的回调函数作为参数，如果该用户名与密码允许访问网站，那么中间件函数返回true
bodyParser用于解析客户端青丘的body中的内容，内部使用JSON编码处理、url编码处理及对于文件的上传处理。
compress使用gzip算法或deflate算法压缩响应流数据，该中间件应该在其他中间件之前使用，以保证所有的响应数据都是被压缩的。
cookieParser解析web浏览器发送的cookie中的内容
csrf通过一个附加的表单变量来对客户端的请求进行处理，以防止网站接收伪跨站点请求。需要结合session中间件与bodyParser中间件一起使用
directory用于在浏览器中列出网站某个目录下的所有子目录及文件，可通过选项的使用来指定是否显示隐藏文件或图标
errorHandler捕捉应用程序中的错误，可将错误作为标准错误输出流进行输出，也可想客户端返回错误。
favicon为浏览器提供收藏夹图标
limit限制客户端提交的数据的字节数
logger将操作日志作为标准输出流进行输出，或输出到一个文件中
methodOverride结合bodyParser一起使用，为bodyParser中间件提供伪http方法支持
responseTime在服务器响应头中添加一个X-Response-Time响应字段。内容为服务器端生成相应数据的时间，单位为毫秒
router提供高级路由特性
session为保存用户数据而提供一个session管理器
static使客户端可以访问网站中的所有静态文件。允许部分下载与指定文件过期时间